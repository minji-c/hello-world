SELECT * FROM 학생명단;
-- 교수명단 테이블의 모든 칼럼을 선택한 후 출력
SELECT * FROM 교수명단;

-- 6. 
-- 학생명단 테이블의 이름과 전공 칼럼을 선택한 후 출력
SELECT 이름, 전공 FROM 학생명단;

-- 7.
-- 학생명단 테이블에서 학년이 3학년 이상인 학생의 경우에만 학번, 이름, 학년 칼럼을 출력
SELECT 학번, 이름, 학년 FROM 학생명단
WHERE 학년 >= 3;

-- 8.
-- 교수명단 테이블에서 교수번호가 1인 경우에만 이름, 전화번호, 소속전공 칼럼을 출력
SELECT 이름, 전화번호, 소속전공 FROM 교수명단
WHERE 번호 = 1;

-- 9. 
-- 교수명단 테이블에서 나이가 35세 이상인 경우에만 이름, 나이 칼럼을 출력
SELECT 이름, 나이 FROM 교수명단
WHERE 나이 >= 35;

