-- 조민지 SQL 과제제출

-- 1.
--학생명단 테이블 생성
CREATE TABLE "학생명단" (
-- 학번칼럼을 10바이트 크기의 가변형 문자열로 지정하고 기본키로 생성
학번 varchar(10) PRIMARY KEY,
-- 이름칼럼을 10바이트 크기의 가변형 문자열로 지정하고 NULL로 지정하지 못하도록하여 생성
이름 varchar(10) NOT NULL,
-- 학년칼럼을 최대 한 자리 숫자로 지정하고 생성
학년 number(1),
-- 전공칼럼을 10바이트 크기의 가변형 문자열로 지정하고 생성
전공 varchar(10)
);

-- 2.
-- 교수명단 테이블 생성
CREATE TABLE "교수명단" (
-- 번호칼럼을 10바이트 크기의 가변형 문자열로 지정하고 기본키로 생성
번호 varchar(10) PRIMARY KEY,
-- 이름칼럼을 10바이트 크기의 가변형 문자열로 지정하고 NULL로 지정하지 못하도록하여 생성
이름 varchar(10) NOT NULL,
-- 전화번호 칼럼을 10바이트 크기의 가변형 문자열로 지정하고 NULL로 지정하지 못하도록하여 생성
전화번호 varchar(15) NOT NULL,
-- 소속전공 칼럼을 10바이트 크기의 가변형 문자열로 지정하고 NULL로 지정하지 못하도록하여 생성
소속전공 varchar(10) NOT NULL,
-- 나이칼럼을 최대 세 자리 숫자로 지정하고 생성
나이 number(3)
);

